// ********** Functions **********

function hello() {
  console.log("Hello function ");
}
hello();

function parameter(a, b) {
  console.log(a + b);
}

parameter(2, 3);

// Arrow function:-  anonymous functions i.e. functions without a name but they are
// often assigned to any variable.

let sum = (a, b) => a + b;
console.log(sum(4, 3));

// ********** Arrays **********

let arr = [1, 2, 3, 4, 5];
console.log(arr);
